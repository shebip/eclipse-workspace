package in.vamsoft.training.general;

/**
 * Class name is Math.
 * @author vamsoft
 *
 */


public class Math {
  
  /**
   * Method to add integer numbers.
   * @param a
   * @param b
   * @return
   */

  public int sum(int a, int b) {
    return a + b;
  }
  
  
/**
 * Method to add double numbers.
 * @param x
 * @param y
 * @return
 */
  public double sum(double x, double y) {
    return x + y;

  }

}
